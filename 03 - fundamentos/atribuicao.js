
const a = 7

let b = 3

b += a     // atribuicao aditiva -> b = b + a

console.log(b)  // 10

//---

b -= 4     // atribuicao subtrativa -> b = b - 4

console.log(b)   // 6


//---

b *= 2    // atribuicao multiplicativa -> b = b * 2

console.log(b)   // 12

//---

b /= 2    // atribuicao divisiva -> b = b / 2

console.log(b)   // 6

//---

b %= 2    // atribuicao de modulo -> b = b % 2  

console.log(b)   // 0



